package chapter1.the_basics.lesson5.binary_operator;

public class LogicalOperator {
    public static void main(String[] args) {
        //Logical Operator => &&, ||

        boolean result1 = 3 > 4 && 5 < 10;
        System.out.println(result1);

        result1 = 3 > 4 || 5 < 10;
        System.out.println(result1);


    }
}
