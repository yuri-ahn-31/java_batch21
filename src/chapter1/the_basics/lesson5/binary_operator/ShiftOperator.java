package chapter1.the_basics.lesson5.binary_operator;

public class ShiftOperator {
    public static void main(String[] args) {
        //Shift Operator
        /**
         * i. left shift (<<)
         * ii. right shift (>>)
         * iii. right shift zero fill
         */

        //left shift so shift mk 2 power nk multiply binary pyoung
        int result3 = 8 << 2;
        System.out.println(result3);

        //right shift so shift nk 2 power nk srrr binary pyoung
        result3 = 8 >> 2;
        System.out.println(result3);
    }
}
