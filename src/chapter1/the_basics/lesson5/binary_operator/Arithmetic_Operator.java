package chapter1.the_basics.lesson5.binary_operator;

public class Arithmetic_Operator {
    public static void main(String[] args) {

        //Binary Operator Operand ( 2 )
        //Arithmetic Operator
        int a = 20;
        int b = 11;
        int result;

        //plus operation (a + b => operation)
        result = a + b; // this is called statement
        System.out.println(result);

        //minus operation
        result = a - b;
        System.out.println(result);

        //multiply
        result = a * b;
        System.out.println(result);

        //division(/) and modulo(%)
        result = a / b ;
        System.out.println(result);

        result = a % b;
        System.out.println(result);




        /**
         * Binary Operator
         *  1. Arithmetic Operator => + - *(x) / %
         *  2. Comparison Operator (Relational Operator) => >, <, <=, >=, ==, !=
         *  3. Logical Operator
         *  4. Bitwise Operator
         *  5. Shift Operator
         *  6. Assignment Operator
         */
    }
}
